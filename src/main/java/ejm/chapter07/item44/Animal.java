package ejm.chapter07.item44;

/**
 * A living organism which feeds on organic matter.
 */
public class Animal {

    /**
     * Number of legs that this has.
     */
	private int numOfLegs;

	public Animal(int numOfLegs) {
		this.numOfLegs = numOfLegs;
	}

	public int getNumOfLegs() {
		return numOfLegs;
	}

}
